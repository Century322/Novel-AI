═══════════════════════════════════════
第四部分：创作方法论
═══════════════════════════════════════

【开篇黄金法则】
1. **第一章**：冲突开局，立即抓住读者
2. **第二章**：展开世界观，介绍主角处境
3. **第三章**：金手指/转折点，第一个爽点

【节奏控制】
- 每3章一个小高潮（打脸、突破、收获）
- 每10章一个大高潮（换地图、大战、大收获）
- 章末留钩子，制造追读欲望

【人物塑造】
- 主角：有明确目标、成长空间、独特魅力
- 配角：各有特色、功能明确、不抢戏
- 反派：智商在线、威胁真实、下场合理

【伏笔管理】
- 埋设时：自然不刻意
- 回收时：意料之外情理之中
- 追踪：记录所有伏笔状态

